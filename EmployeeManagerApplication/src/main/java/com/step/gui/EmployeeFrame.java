    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      if(this.jTable1.getSelectedColumn()!= -1){
      int idEmployee = Integer.parseInt(tableModel.getValueAt(this.jTable1.getSelectedRow(),0).toString());
      String name = String.valueOf(tableModel.getValueAt(this.jTable1.getSelectedRow(), 1));
      String surname = String.valueOf(tableModel.getValueAt(this.jTable1.getSelectedRow(), 2));
      int age = Integer.parseInt(tableModel.getValueAt(this.jTable1.getSelectedRow(), 3).toString());
      Genre genre = Genre.valueOf(tableModel.getValueAt(this.jTable1.getSelectedRow(), 4).toString());
      LocalDate birthdate = LocalDate.parse(tableModel.getValueAt(this.jTable1.getSelectedRow(), 5).toString());
      int identitycode = Integer.parseInt(tableModel.getValueAt(this.jTable1.getSelectedRow(), 6).toString());
      editEmployee.setValueEmployee(idEmployee,name, surname, age, identitycode, genre, birthdate);
      editEmployee.setVisible(true);
      }
      else
          JOptionPane.showMessageDialog(null, "ERROR: No selected row, please selected row "," ERROR !!! ", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      
         if(this.jTable1.getSelectedRow()!= -1){
            int indexEmployee = (int)tableModel.getValueAt(this.jTable1.getSelectedRow(),0);
           String name  = String.valueOf(this.tableModel.getValueAt(this.jTable1.getSelectedRow(), 1));
           String surname = String.valueOf(String.valueOf(this.tableModel.getValueAt(this.jTable1.getSelectedRow(), 2)));
           
           int yesNo = JOptionPane.showConfirmDialog(null, "Are you sure you want to remove the row?", "Please confirm", JOptionPane.YES_NO_OPTION);
            if (yesNo == JOptionPane.YES_OPTION) {
              tableModel.deletedEmployeeDb(indexEmployee);
            JOptionPane.showMessageDialog(null,"SUCCES: You deleted employee with successfully ".concat(name)+" ".concat(surname)," INFORMATION !!!",JOptionPane.INFORMATION_MESSAGE);  
            }
        } else {
            JOptionPane.showMessageDialog(null,"ERROR: Select row first", "ERROR !!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

