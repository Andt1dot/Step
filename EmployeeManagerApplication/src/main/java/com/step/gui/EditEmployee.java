    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = this.jTextField1.getText();
        String surname = this.jTextField2.getText();
        int age = Integer.parseInt(this.jTextField3.getText());
        Genre genre = Genre.valueOf(this.jComboBox1.getSelectedItem().toString());
        int identitycode =  Integer.parseInt(this.jTextField4.getText());
        LocalDate birthdate = jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        boolean isEdited =  tabelModel.editEmployeeDb(new Employee(indexEmployee,name,surname,genre,age,identitycode,birthdate));
        if (isEdited) 
        JOptionPane.showMessageDialog(null,"SUCCES: You edited employee with successfully", "INFORMATION !!!",JOptionPane.INFORMATION_MESSAGE);  
        else
        JOptionPane.showConfirmDialog(null,"ERROR: Editing is not possible please repeat or contact you programmer !!!", " ERROR !!!", JOptionPane.ERROR_MESSAGE);                      
        this.setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

  
